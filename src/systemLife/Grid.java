    private int size, DEFAULT_SIZE = 22;

    public  Grid(){
        size = DEFAULT_SIZE;
        runningColony = new int[size][size];
        for(int i = 0; i < size; i++){
            for(int j = 0; j < size; j++){
                runningColony[i][j] = 0;
            }
        }
    }

    public  Grid(int inSize){
        size = inSize;
        runningColony = new int[size][size];
        for(int i = 0; i < size; i++){
            for(int j = 0; j < size; j++){
                runningColony[i][j] = 0;
            }
        }
    }


    public void setSize(int inSize){
        System.out.println("In Grid setSize()");
        size = inSize + 2;
        runningColony = new int[size][size];
        for(int i = 0; i < size; i++){
            for(int j = 0; j < size; j++){
                runningColony[i][j] = 0;
            }
        }

    }

    public void print(){
        System.out.println("In Grid print()");
        for (int i = 0; i < size - 2; i++)
        {
            for (int j = 0; j < size - 2; j++)
            {
                // the +1 is to adjust the starting point at [1][1] as top-left corner
                if (runningColony[i + 1][j + 1] == 0)
                    System.out.print(".");
                else {
                    System.out.print("*");
                }
            }
            System.out.println();
        }
    }

    public void setCell(int cellNumber){
        System.out.println("In Grid setCell()");

        int num = size - 2;
        int x,y;
        if(cellNumber%(num) == 0) x = num;
        else x = cellNumber%(num);
        if(cellNumber%(num) == 0)y = cellNumber/(num);
        else y = cellNumber/(num) + 1;

        if(runningColony[y][x] == 0) runningColony[y][x] = 1;
        else runningColony[y][x] = 0;
    }

    public void setRunningColony(int[][] newColony){
        System.out.println("In Grid setRunningColony()");
        runningColony = newColony;
        size = newColony.length;
    }

